// Parse sprite string into 2D array
function parseSprite(spriteStr) {
    return spriteStr
        .trim()
        .split('\n')
        .map(row => row.trim().split(''));
}

// Render sprite to canvas
function renderSprite(ctx, spriteData, x, y, scale = 4) {
    const pixels = parseSprite(spriteData);

    pixels.forEach((row, py) => {
        row.forEach((pixel, px) => {
            if (pixel !== '.' && PALETTE[pixel]) {
                ctx.fillStyle = PALETTE[pixel];
                ctx.fillRect(
                    x + px * scale,
                    y + py * scale,
                    scale,
                    scale
                );
            }
        });
    });
}

